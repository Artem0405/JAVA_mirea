public class Student {
    private String rollNo; // Поле для хранения номера студенческого билета
    private String name; // Поле для хранения имени студента

    public String getRollNo() {
        return rollNo; // Возвращает номер студенческого билета
    }

    public void setRollNo(String rollNo) {
        this.rollNo = rollNo; // Устанавливает номер студенческого билета
    }

    public String getName() {
        return name; // Возвращает имя студента
    }

    public void setName(String name) {
        this.name = name; // Устанавливает имя студента
    }
}
